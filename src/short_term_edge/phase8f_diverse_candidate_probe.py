from __future__ import annotations

import json
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import pandas as pd

from .phase5n import _finite_float
from .strategy_spec import EntryRule, ExitRule, RiskRule, StrategySpec


@dataclass(frozen=True)
class Phase8FConfig:
    max_specs: int = 3
    min_trades: int = 60
    drawdown_limit: float = -2_500.0
    concentration_limit: float = 0.35


def select_phase8f_specs(event_results: pd.DataFrame, config: Phase8FConfig = Phase8FConfig()) -> list[StrategySpec]:
    candidates = event_results[event_results["phase8e_label"].eq("backtest_candidate")].sort_values("phase8e_score", ascending=False)
    specs: list[StrategySpec] = []
    used_keys: set[tuple[str, str]] = set()
    for _, row in candidates.iterrows():
        mapped = _spec_from_event_row(row)
        if mapped is None:
            continue
        key = (mapped.instrument, mapped.family)
        if key in used_keys:
            continue
        specs.append(mapped)
        used_keys.add(key)
        if len(specs) >= config.max_specs:
            break
    return specs


def rank_phase8f_results(candidate_summary: pd.DataFrame) -> pd.DataFrame:
    if candidate_summary.empty:
        return candidate_summary.copy()
    reusable = candidate_summary.drop(columns=[column for column in candidate_summary.columns if column.startswith(("phase5n_", "phase8f_"))], errors="ignore")
    rows: list[dict[str, Any]] = []
    for _, row in reusable.iterrows():
        out = row.to_dict()
        out["phase8f_score"] = _phase8f_score(out)
        out["phase8f_label"] = _phase8f_label(out)
        out["phase8f_notes"] = _phase8f_notes(out)
        rows.append(out)
    ranked = pd.DataFrame(rows).sort_values(["phase8f_score", "slippage_4_ticks_net_pnl", "net_pnl"], ascending=[False, False, False]).reset_index(drop=True)
    ranked.insert(0, "phase8f_rank", range(1, len(ranked) + 1))
    return ranked


def write_phase8f_specs(specs: list[StrategySpec], path: Path) -> None:
    path.write_text(json.dumps([json.loads(spec.to_json()) | {"canonical_id": spec.canonical_id()} for spec in specs], indent=2, sort_keys=True), encoding="utf-8")


