def _spec_from_event_row(row: pd.Series) -> StrategySpec | None:
    instrument = str(row["instrument"])
    timeframe = int(row["timeframe"])
    side_filter = _side_filter(str(row["side"]))
    family = str(row["family"])
    tick_params = _tick_params(instrument)
    risk = RiskRule("one_open_position", {"max_trades_per_day": 2, "side_filter": side_filter})
    if family == "vwap_pullback_continuation":
        return StrategySpec(
            instrument=instrument,
            family="vwap_pullback_continuation",
            timeframe=timeframe,
            entry=EntryRule("vwap_pullback", {"pullback_ref": "vwap", "pullback_ticks": tick_params["pullback_ticks"], "start_minute": 15, "min_slope_ticks": 0}),
            exit=ExitRule("fixed_ticks", {"stop_ticks": tick_params["stop_ticks"], "target_ticks": tick_params["target_ticks"]}),
            risk=risk,
            notes="Phase 8F diverse probe from Phase 8E event scout: VWAP pullback continuation.",
        ).validate()
    if family == "vwap_reclaim_rejection":
        mode = "both" if side_filter == "both" else ("reclaim" if side_filter == "long" else "rejection")
        return StrategySpec(
            instrument=instrument,
            family="vwap_reclaim_rejection",
            timeframe=timeframe,
            entry=EntryRule("vwap_cross", {"mode": mode}),
            exit=ExitRule("fixed_ticks", {"stop_ticks": tick_params["stop_ticks"], "target_ticks": tick_params["target_ticks"]}),
            risk=risk,
            notes="Phase 8F diverse probe from Phase 8E event scout: VWAP reclaim/rejection.",
        ).validate()
    if family == "opening_range_fade":
        return StrategySpec(
            instrument=instrument,
            family="opening_range_failure",
            timeframe=timeframe,
            entry=EntryRule("close_back_inside", {"or_minutes": 15, "target": "opposite", "min_range": tick_params["min_range"]}),
            exit=ExitRule("range_target", {"target": "opposite"}),
            risk=risk,
            notes="Phase 8F diverse probe from Phase 8E event scout: opening-range fade mapped to opening_range_failure.",
        ).validate()
    if family == "opening_range_breakout":
        return StrategySpec(
            instrument=instrument,
            family="opening_range_breakout",
            timeframe=timeframe,
            entry=EntryRule("close_outside_range", {"or_minutes": 15, "min_range": tick_params["min_range"]}),
            exit=ExitRule("r_multiple", {"target": "1R", "stop_mode": "half_range"}),
            risk=RiskRule("one_open_position", {"max_trades_per_day": 1, "side_filter": side_filter}),
            notes="Phase 8F diverse probe from Phase 8E event scout: opening-range breakout.",
        ).validate()
    return None


def _side_filter(side: str) -> str:
    if side == "long_only":
        return "long"
    if side == "short_only":
        return "short"
    return "both"


def _tick_params(instrument: str) -> dict[str, float | int]:
    if instrument == "MNQ":
        return {"stop_ticks": 40, "target_ticks": 60, "pullback_ticks": 8, "min_range": 10.0}
    return {"stop_ticks": 12, "target_ticks": 18, "pullback_ticks": 3, "min_range": 1.0}


def _phase8f_score(row: dict[str, Any]) -> float:
    net = _finite_float(row.get("net_pnl", 0.0), 0.0)
    stress = _finite_float(row.get("slippage_4_ticks_net_pnl", 0.0), 0.0)
    validation = _finite_float(row.get("validation_pnl", 0.0), 0.0)
    holdout = _finite_float(row.get("holdout_pnl", 0.0), 0.0)
    drawdown = _finite_float(row.get("max_drawdown", 0.0), 0.0)
    day = _finite_float(row.get("best_day_concentration", 1.0), 1.0)
    trade = _finite_float(row.get("best_trade_concentration", 1.0), 1.0)
    trades = _finite_float(row.get("trades", 0), 0.0)
    active = _finite_float(row.get("active_session_pct", 0.0), 0.0)
    score = 0.0
    score += max(min(net / 4_000.0, 2.0), -2.0) * 16.0
    score += max(min(stress / 4_000.0, 2.0), -2.0) * 26.0
    score += max(min(validation / 1_500.0, 2.0), -2.0) * 8.0
    score += max(min(holdout / 1_500.0, 2.0), -2.0) * 14.0
    score += min(trades / 150.0, 1.0) * 8.0
    score += min(active, 0.50) * 8.0
    score -= min(abs(drawdown) / 2_500.0, 2.0) * 16.0
    score -= max(day - 0.35, 0.0) * 150.0
    score -= max(trade - 0.25, 0.0) * 150.0
    return round(float(score), 4)


def _phase8f_label(row: dict[str, Any]) -> str:
    notes = _phase8f_notes(row)
    if not notes:
        return "phase8f_diverse_probe_candidate"
    if _finite_float(row.get("net_pnl", 0.0), 0.0) > 0 and _finite_float(row.get("slippage_4_ticks_net_pnl", 0.0), 0.0) > 0:
        return "phase8f_watchlist_needs_filter"
    return "rejected"


def _phase8f_notes(row: dict[str, Any], config: Phase8FConfig = Phase8FConfig()) -> str:
    notes: list[str] = []
    if _finite_float(row.get("net_pnl", 0.0), 0.0) <= 0:
        notes.append("negative net PnL")
    if _finite_float(row.get("slippage_4_ticks_net_pnl", 0.0), 0.0) <= 0:
        notes.append("fails 4-tick stress")
    if int(_finite_float(row.get("trades", 0), 0.0)) < config.min_trades:
        notes.append("too few trades")
    if _finite_float(row.get("max_drawdown", 0.0), 0.0) < config.drawdown_limit:
        notes.append("drawdown exceeds diverse-probe cap")
    if _finite_float(row.get("best_day_concentration", 1.0), 1.0) > config.concentration_limit:
        notes.append("one-day concentration risk")
    if _finite_float(row.get("best_trade_concentration", 1.0), 1.0) > config.concentration_limit:
        notes.append("one-trade concentration risk")
    if _finite_float(row.get("validation_pnl", 0.0), 0.0) < 0:
        notes.append("negative validation split")
    if _finite_float(row.get("holdout_pnl", 0.0), 0.0) < 0:
        notes.append("negative holdout split")
    return "; ".join(notes) if notes else "survives bounded diverse probe; requires walk-forward validation"
