def _replay_combo_policy(combo_id: str, trades: pd.DataFrame, policy: Phase7DPolicy, config: Phase7DConfig) -> dict[str, Any]:
    day_pnl = trades.groupby("trading_session", sort=True)["net_pnl"].sum()
    multiplier = policy.funded_quantity / config.source_quantity
    cumulative = 0.0
    peak = 0.0
    evaluation_pass_date = ""
    payout_date = ""
    breach_date = ""
    phase = "evaluation"
    funded_profit_days = 0
    payout_cycle_pnl = 0.0
    max_loss_remaining_at_payout = 0.0
    lockout_day_count = 0
    profit_target_lockout_count = 0
    loss_lockout_count = 0
    processed_days = 0
    for session, raw_pnl in day_pnl.items():
        processed_days += 1
        scaled = float(raw_pnl) * multiplier
        adjusted = _apply_daily_policy(scaled, policy)
        if adjusted != scaled:
            lockout_day_count += 1
            if policy.daily_profit_target is not None and scaled > policy.daily_profit_target:
                profit_target_lockout_count += 1
            if policy.daily_loss_lockout is not None and scaled < -abs(policy.daily_loss_lockout):
                loss_lockout_count += 1
        cumulative += adjusted
        peak = max(peak, cumulative)
        drawdown = peak - cumulative
        if drawdown >= config.max_loss_limit:
            breach_date = str(session)
            break
        if phase == "evaluation" and cumulative >= config.evaluation_target:
            phase = "funded"
            evaluation_pass_date = str(session)
            payout_cycle_pnl = 0.0
            funded_profit_days = 0
            continue
        if phase == "funded":
            payout_cycle_pnl += adjusted
            if adjusted > 0:
                funded_profit_days += 1
            estimated_payout = max(payout_cycle_pnl * config.trader_profit_split, 0.0)
            if estimated_payout >= config.payout_threshold and funded_profit_days >= config.min_funded_profit_days:
                payout_date = str(session)
                max_loss_remaining_at_payout = max(config.max_loss_limit - drawdown, 0.0)
                break
    estimated_payout = max(payout_cycle_pnl * config.trader_profit_split, 0.0) if payout_date else 0.0
    success = bool(payout_date)
    return {
        "combo_id": combo_id,
        "policy_id": policy.policy_id,
        "funded_quantity": policy.funded_quantity,
        "daily_profit_target": "none" if policy.daily_profit_target is None else policy.daily_profit_target,
        "daily_loss_lockout": "none" if policy.daily_loss_lockout is None else policy.daily_loss_lockout,
        "success": success,
        "evaluation_pass_date": evaluation_pass_date,
        "payout_date": payout_date,
        "estimated_payout": float(estimated_payout),
        "processed_days": processed_days,
        "max_loss_remaining_at_payout": float(max_loss_remaining_at_payout),
        "final_pnl": float(cumulative),
        "breach_date": breach_date,
        "lockout_day_count": int(lockout_day_count),
        "profit_target_lockout_count": int(profit_target_lockout_count),
        "loss_lockout_count": int(loss_lockout_count),
        "same_bar_stop_target_ambiguity_count": int(trades["same_bar_stop_target_ambiguity"].fillna(0).sum()),
        "component_trade_counts": trades["component_family"].value_counts().to_json(),
        "phase7d_notes": _phase7d_notes(success, evaluation_pass_date, payout_date, breach_date, trades),
    }


def _apply_daily_policy(daily_pnl: float, policy: Phase7DPolicy) -> float:
    adjusted = daily_pnl
    if policy.daily_profit_target is not None:
        adjusted = min(adjusted, float(policy.daily_profit_target))
    if policy.daily_loss_lockout is not None:
        adjusted = max(adjusted, -abs(float(policy.daily_loss_lockout)))
    return adjusted


def _phase7d_notes(success: bool, evaluation_pass_date: str, payout_date: str, breach_date: str, trades: pd.DataFrame) -> str:
    notes: list[str] = []
    if success:
        notes.append("hits simplified payout-path objective on matched window")
    elif breach_date:
        notes.append("max-loss breach before payout objective")
    elif not evaluation_pass_date:
        notes.append("does not pass evaluation target")
    else:
        notes.append("passes evaluation but does not reach payout threshold")
    ambiguity = int(trades["same_bar_stop_target_ambiguity"].fillna(0).sum())
    if ambiguity:
        notes.append("same-bar ambiguity remains; still not promotable")
    return "; ".join(notes)


def _fmt_num(value: float) -> str:
    return str(int(value)) if float(value).is_integer() else str(value).replace(".", "p")
