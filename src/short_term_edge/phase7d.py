from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Any

import pandas as pd


@dataclass(frozen=True)
class Phase7DPolicy:
    funded_quantity: int
    daily_profit_target: float | None
    daily_loss_lockout: float | None

    @property
    def policy_id(self) -> str:
        profit = "none" if self.daily_profit_target is None else _fmt_num(self.daily_profit_target)
        loss = "none" if self.daily_loss_lockout is None else _fmt_num(self.daily_loss_lockout)
        return f"q{self.funded_quantity}_pt{profit}_ll{loss}"


@dataclass(frozen=True)
class Phase7DConfig:
    legacy_start: str = "2025-12-29"
    legacy_end: str = "2026-06-26"
    source_quantity: int = 1
    funded_quantities: tuple[int, ...] = (1, 2)
    daily_profit_targets: tuple[float | None, ...] = (None, 200.0)
    daily_loss_lockouts: tuple[float | None, ...] = (None, 300.0)
    evaluation_target: float = 2_000.0
    max_loss_limit: float = 1_000.0
    payout_threshold: float = 500.0
    trader_profit_split: float = 0.50
    min_funded_profit_days: int = 5

    def validate(self) -> "Phase7DConfig":
        if self.source_quantity <= 0:
            raise ValueError("source_quantity must be positive")
        if any(quantity <= 0 for quantity in self.funded_quantities):
            raise ValueError("funded_quantities must be positive")
        if self.evaluation_target <= 0 or self.max_loss_limit <= 0 or self.payout_threshold <= 0:
            raise ValueError("evaluation_target, max_loss_limit, and payout_threshold must be positive")
        if not 0 < self.trader_profit_split <= 1:
            raise ValueError("trader_profit_split must be in (0, 1]")
        return self


def phase7d_policy_grid(config: Phase7DConfig = Phase7DConfig()) -> list[Phase7DPolicy]:
    config.validate()
    return [
        Phase7DPolicy(quantity, profit_target, loss_lockout)
        for quantity in config.funded_quantities
        for profit_target in config.daily_profit_targets
        for loss_lockout in config.daily_loss_lockouts
    ]


def run_phase7d_payout_diagnostic(trade_log_path: Path, config: Phase7DConfig = Phase7DConfig()) -> pd.DataFrame:
    config.validate()
    if not trade_log_path.exists():
        raise FileNotFoundError(f"Phase 7D requires Phase 7B trade log at {trade_log_path}")
    trades = pd.read_csv(trade_log_path)
    if trades.empty:
        return pd.DataFrame()
    required = {"combo_id", "trading_session", "net_pnl", "same_bar_stop_target_ambiguity", "component_family"}
    missing = sorted(required.difference(trades.columns))
    if missing:
        raise ValueError(f"Phase 7D trade log is missing required columns: {missing}")
    scoped = trades.copy()
    scoped["trading_session"] = pd.to_datetime(scoped["trading_session"]).dt.date.astype(str)
    scoped = scoped[(scoped["trading_session"] >= config.legacy_start) & (scoped["trading_session"] <= config.legacy_end)]
    rows: list[dict[str, Any]] = []
    for combo_id, combo_trades in scoped.groupby("combo_id", sort=True):
        for policy in phase7d_policy_grid(config):
            rows.append(_replay_combo_policy(str(combo_id), combo_trades, policy, config))
    if not rows:
        return pd.DataFrame()
    out = pd.DataFrame(rows)
    out = out.sort_values(["success", "estimated_payout", "max_loss_remaining_at_payout", "final_pnl"], ascending=[False, False, False, False]).reset_index(drop=True)
    out.insert(0, "phase7d_rank", range(1, len(out) + 1))
    return out


