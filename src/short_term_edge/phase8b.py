from __future__ import annotations

from collections import Counter
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Mapping

import pandas as pd


@dataclass(frozen=True)
class Phase8BConfig:
    phase7c_path: Path = Path("outputs/phase7c_assumption_drift.csv")
    phase7d_path: Path = Path("outputs/phase7d_payout_diagnostic_results.csv")
    phase8a_path: Path = Path("outputs/phase8a_mgc_clean_family_results.csv")
    phase8a_manifest_path: Path = Path("artifacts/phase8a_mgc_clean_family/phase8a-r1-smoke/manifest.json")
    symbol: str = "MGC"
    overtrading_active_session_threshold: float = 0.75
    overtrading_trade_threshold: int = 500
    concentration_threshold: float = 0.50
    drawdown_limit: float = -1_000.0


@dataclass(frozen=True)
class Phase8BResult:
    failure_summary: pd.DataFrame
    phase7c_rows: int
    phase7c_high_severity_count: int
    phase7d_rows: int
    phase7d_success_count: int
    phase8a_scored_count: int
    phase8a_selected_specs_count: int
    phase8a_label_counts: dict[str, int]
    recommended_next_step: str
    decision: str

    def to_manifest_payload(self) -> dict[str, Any]:
        return {
            "phase7c_rows": self.phase7c_rows,
            "phase7c_high_severity_count": self.phase7c_high_severity_count,
            "phase7d_rows": self.phase7d_rows,
            "phase7d_success_count": self.phase7d_success_count,
            "phase8a_scored_count": self.phase8a_scored_count,
            "phase8a_selected_specs_count": self.phase8a_selected_specs_count,
            "phase8a_label_counts": self.phase8a_label_counts,
            "recommended_next_step": self.recommended_next_step,
            "decision": self.decision,
            "top_failure_categories": self.failure_summary.head(5).to_dict(orient="records"),
        }


def synthesize_phase8b_failures(
    phase7c: pd.DataFrame,
    phase7d: pd.DataFrame,
    phase8a: pd.DataFrame,
    *,
    phase8a_manifest: Mapping[str, Any] | None = None,
    config: Phase8BConfig = Phase8BConfig(),
) -> Phase8BResult:
    phase8a_manifest = phase8a_manifest or {}
    phase7c_counts = _phase7c_counts(phase7c)
    phase7d_counts = _note_counts(phase7d, "phase7d_notes")
    phase8a_counts = _phase8a_counts(phase8a, config)
    categories = sorted(set(phase7c_counts) | set(phase7d_counts) | set(phase8a_counts))
    rows = []
    for category in categories:
        phase7c_count = phase7c_counts.get(category, 0)
        phase7d_count = phase7d_counts.get(category, 0)
        phase8a_count = phase8a_counts.get(category, 0)
        total = phase7c_count + phase7d_count + phase8a_count
        rows.append(
            {
                "failure_category": category,
                "phase7c_count": int(phase7c_count),
                "phase7d_count": int(phase7d_count),
                "phase8a_count": int(phase8a_count),
                "total_count": int(total),
                "interpretation": _category_interpretation(category),
            }
        )
    failure_summary = pd.DataFrame(
        rows,
        columns=["failure_category", "phase7c_count", "phase7d_count", "phase8a_count", "total_count", "interpretation"],
    ).sort_values(["total_count", "failure_category"], ascending=[False, True], ignore_index=True)

    label_counts = _phase8a_label_counts(phase8a, phase8a_manifest)
    selected_specs_count = _manifest_int(phase8a_manifest, "selected_specs_count", len(phase8a))
    decision = (
        "Stop entry-variant grinding; use Phase 8B evidence to design pre-entry no-trade/session-selection filters "
        "before spending more MGC compute."
    )
    return Phase8BResult(
        failure_summary=failure_summary,
        phase7c_rows=int(len(phase7c)),
        phase7c_high_severity_count=_high_severity_count(phase7c),
        phase7d_rows=int(len(phase7d)),
        phase7d_success_count=_success_count(phase7d),
        phase8a_scored_count=int(len(phase8a)),
        phase8a_selected_specs_count=selected_specs_count,
        phase8a_label_counts=label_counts,
        recommended_next_step="phase8c_no_trade_session_filters",
        decision=decision,
    )


