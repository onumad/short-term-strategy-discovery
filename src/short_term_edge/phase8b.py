def _phase7c_counts(phase7c: pd.DataFrame) -> Counter[str]:
    counter: Counter[str] = Counter()
    for _, row in phase7c.iterrows():
        axis_text = " ".join(str(row.get(column, "")) for column in ("axis", "drift", "recommended_action"))
        category = _category_for_text(axis_text)
        if category:
            counter[category] += 1
    return counter


def _note_counts(results: pd.DataFrame, notes_column: str) -> Counter[str]:
    counter: Counter[str] = Counter()
    if notes_column not in results.columns:
        return counter
    for raw_notes in results[notes_column].fillna(""):
        for note in str(raw_notes).split(";"):
            category = _category_for_text(note)
            if category:
                counter[category] += 1
    return counter


def _phase8a_counts(phase8a: pd.DataFrame, config: Phase8BConfig) -> Counter[str]:
    counter = _note_counts(phase8a, "phase8a_notes")
    if "risk_notes" in phase8a.columns:
        counter.update(_note_counts(phase8a, "risk_notes"))
    for _, row in phase8a.iterrows():
        if _float(row.get("net_pnl")) < 0:
            counter["negative_expectancy"] += 1
        if _float(row.get("slippage_4_ticks_net_pnl")) < 0:
            counter["cost_slippage"] += 1
        if _float(row.get("active_session_pct")) >= config.overtrading_active_session_threshold or _float(row.get("trades")) >= config.overtrading_trade_threshold:
            counter["overtrading"] += 1
        if _float(row.get("max_drawdown")) <= config.drawdown_limit:
            counter["drawdown"] += 1
        if _float(row.get("best_day_concentration")) >= config.concentration_threshold:
            counter["concentration"] += 1
        if _float(row.get("best_trade_concentration")) >= config.concentration_threshold:
            counter["concentration"] += 1
        if _float(row.get("same_bar_stop_target_ambiguity_count")) > 0:
            counter["ambiguity"] += 1
        if _float(row.get("validation_pnl")) < 0:
            counter["split_instability"] += 1
        if _float(row.get("holdout_pnl")) < 0:
            counter["split_instability"] += 1
    return counter


def _category_for_text(text: object) -> str:
    lowered = str(text).lower()
    if not lowered.strip():
        return ""
    if "not promotable" in lowered:
        return ""
    if "concentration" in lowered:
        return "concentration"
    if "slippage" in lowered or "cost" in lowered:
        return "cost_slippage"
    if "drawdown" in lowered or "max-loss" in lowered or "max loss" in lowered:
        return "drawdown"
    if "same-bar" in lowered or "ambiguity" in lowered:
        return "ambiguity"
    if "validation" in lowered or "holdout" in lowered or "split" in lowered:
        return "split_instability"
    if "data window" in lowered or "date-window" in lowered or "matched-window" in lowered:
        return "data_window_drift"
    if "objective" in lowered or "evaluation" in lowered or "payout" in lowered:
        return "objective_drift"
    if "phase" in lowered or "lockout" in lowered or "policy" in lowered:
        return "policy_drift"
    if "negative net" in lowered or "negative pnl" in lowered:
        return "negative_expectancy"
    return "other"


def _category_interpretation(category: str) -> str:
    return {
        "ambiguity": "Intrabar uncertainty remains a research blocker, not a tuning problem.",
        "concentration": "PnL is too dependent on one day/trade; require distribution filters before promotion.",
        "cost_slippage": "Costs/stress erase the apparent edge; fewer higher-quality trades are needed.",
        "data_window_drift": "Legacy and current windows differ; avoid broadening until provenance is explicit.",
        "drawdown": "Loss path breaches strict risk limits before any payout/promotion case is credible.",
        "negative_expectancy": "Raw net performance is negative before advanced validation.",
        "objective_drift": "Payout-style objectives and robustness objectives are not equivalent.",
        "overtrading": "Candidates are active in too many sessions; next work should decide when not to trade.",
        "policy_drift": "Risk policy assumptions changed enough to require diagnostic isolation.",
        "split_instability": "Validation/holdout weakness means the signal is not stable enough for promotion.",
        "other": "Unclassified failure evidence that should be inspected before broadening search.",
    }.get(category, "Unclassified failure evidence.")


def _high_severity_count(phase7c: pd.DataFrame) -> int:
    if "severity" not in phase7c.columns:
        return 0
    return int((phase7c["severity"].fillna("").astype(str).str.lower() == "high").sum())


def _success_count(phase7d: pd.DataFrame) -> int:
    if "success" not in phase7d.columns:
        return 0
    values = phase7d["success"]
    if values.dtype == bool:
        return int(values.sum())
    return int(values.fillna(False).astype(str).str.lower().isin(["true", "1", "yes"]).sum())


def _phase8a_label_counts(phase8a: pd.DataFrame, manifest: Mapping[str, Any]) -> dict[str, int]:
    manifest_counts = manifest.get("label_counts")
    if isinstance(manifest_counts, Mapping):
        return {str(key): int(value) for key, value in manifest_counts.items()}
    if "phase8a_label" not in phase8a.columns:
        return {}
    return {str(key): int(value) for key, value in phase8a["phase8a_label"].fillna("missing").value_counts().sort_index().to_dict().items()}


def _manifest_int(manifest: Mapping[str, Any], key: str, default: int) -> int:
    try:
        return int(manifest.get(key, default))
    except (TypeError, ValueError):
        return int(default)


def _float(value: object) -> float:
    try:
        out = float(value)
    except (TypeError, ValueError):
        return 0.0
    if pd.isna(out):
        return 0.0
    return out
