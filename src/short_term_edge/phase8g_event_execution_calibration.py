@dataclass(frozen=True)
class CalibrationVariant:
    stop_model: str
    target_model: str
    time_stop: int


@dataclass(frozen=True)
class Phase8GConfig:
    max_candidates: int = 3
    entry_delays: tuple[str, ...] = ("next_bar_open", "next_1m_close", "next_3m_close", "next_5m_close")
    variants: tuple[CalibrationVariant, ...] = (
        CalibrationVariant("none", "horizon_close", 15),
        CalibrationVariant("none", "horizon_close", 30),
        CalibrationVariant("fixed_ticks", "1R", 30),
        CalibrationVariant("fixed_ticks", "1.5R", 30),
    )
    concentration_limit: float = 0.35


def select_phase8g_candidates(event_results: pd.DataFrame, config: Phase8GConfig = Phase8GConfig()) -> pd.DataFrame:
    """Select a capped, diversified set of event-study positives for calibration."""
    if event_results.empty:
        return event_results.copy()
    required = {"phase8e_label", "phase8e_score", "instrument", "family", "hypothesis_id"}
    missing = required.difference(event_results.columns)
    if missing:
        raise ValueError(f"Phase 8G event results missing required columns: {sorted(missing)}")
    positives = event_results[event_results["phase8e_label"].eq("backtest_candidate")].sort_values("phase8e_score", ascending=False)
    selected = []
    used_keys: set[tuple[str, str]] = set()
    for _, row in positives.iterrows():
        key = (str(row["instrument"]), str(row["family"]))
        if key in used_keys:
            continue
        selected.append(row)
        used_keys.add(key)
        if len(selected) >= config.max_candidates:
            break
    return pd.DataFrame(selected).reset_index(drop=True)


def run_phase8g_calibration(event_results: pd.DataFrame, data_by_symbol: dict[str, pd.DataFrame], config: Phase8GConfig = Phase8GConfig()) -> pd.DataFrame:
    candidates = select_phase8g_candidates(event_results, config)
    rows: list[dict[str, Any]] = []
    for _, candidate in candidates.iterrows():
        symbol = str(candidate["instrument"])
        bars = data_by_symbol.get(symbol, pd.DataFrame())
        if bars.empty:
            continue
        prepared = _prepare_bars(bars)
        event_positions = _event_positions(prepared, candidate.to_dict())
        for entry_delay in config.entry_delays:
            for variant in config.variants:
                trades = _simulate_calibration_trades(prepared, candidate.to_dict(), event_positions, entry_delay, variant)
                rows.append(_summarize_calibration(candidate.to_dict(), event_positions, entry_delay, variant, trades, config))
    if not rows:
        return pd.DataFrame(columns=_result_columns())
    result = pd.DataFrame(rows)
    result = result.sort_values(["calibration_score", "slippage_4_ticks_net_pnl", "net_pnl"], ascending=[False, False, False]).reset_index(drop=True)
    result.insert(0, "phase8g_rank", range(1, len(result) + 1))
    return result[_result_columns(include_rank=True)]


